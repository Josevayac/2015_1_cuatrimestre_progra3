using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace HolaMundo
{
    public class Operaciones
    {
        public int Suma(int suma1,int suma2)
        {
            return suma1 + suma2;
        }

        public int Resta(int resta1, int resta2)
        {
            return resta1 - resta2;
        }

        public int Multiplicacion(int multiplicacion1, int multiplicacion2)
        {
            return multiplicacion1 * multiplicacion2;
        }

        public int Division(int division1, int division2)
        {
            return division1 / division2;
        }
    }
}
